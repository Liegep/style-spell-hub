const normalizedSpanish = new Map<string, string>(
  Object.entries(appText.es).flatMap(([source, translation]) => [
    [normalizePhrase(source), translation],
    [normalizePhrase(source.toLowerCase()), translation],
    [normalizePhrase(spaceLetters(source)), translation],
    [normalizePhrase(spaceLetters(source.toLowerCase())), translation],
    [normalizePhrase(source.toUpperCase()), translation.toUpperCase()],
    [normalizePhrase(spaceLetters(source.toUpperCase())), translation.toUpperCase()],
  ]),
);

export function translateAppPhrase(value: string, language: Lang) {
  if (language !== "es") return value;
  const normalized = normalizePhrase(value);
  const translated = normalizedSpanish.get(normalized) ?? normalizedSpanish.get(normalized.toLowerCase());
  if (!translated) return value;

  const leading = value.match(/^\s*/)?.[0] ?? "";
  const trailing = value.match(/\s*$/)?.[0] ?? "";
  return `${leading}${translated}${trailing}`;
}

export function hasAppPhrase(value: string) {
  return normalizedSpanish.has(normalizePhrase(value));
}

function normalizePhrase(value: string) {
  return value.replace(/\s+/g, " ").trim();
}

function spaceLetters(value: string) {
  return value
    .split("")
    .map((char) => (/[A-Za-zÀ-ÿ]/.test(char) ? `${char} ` : char))
    .join("")
    .replace(/\s+/g, " ")
    .trim();
}
